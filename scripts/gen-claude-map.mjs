#!/usr/bin/env node
/*
 * Regenerates the auto-managed "Project map" block in the root CLAUDE.md.
 *
 * Deterministic: it only reads the filesystem and rewrites the text between the
 * AUTO-GEN:map markers. No AI, no network — safe to run inside a git pre-commit
 * hook and cheap enough to run on every commit.
 *
 * The map answers "what exists and where" (collections, pipelines, endpoints,
 * pages). The "why" and the conventions live in the hand-written prose sections
 * and are intentionally NOT touched here — see CLAUDE.md section 7.
 *
 * Run manually:  pnpm claude:map   (from web/)  |  node scripts/gen-claude-map.mjs
 */
import { readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const CLAUDE_MD = join(ROOT, 'CLAUDE.md')
const START = '<!-- AUTO-GEN:map START -->'
const END = '<!-- AUTO-GEN:map END -->'

/** Sorted names of a directory's entries matching `filter`; [] if it is missing. */
function listDir(rel, filter) {
  try {
    return readdirSync(join(ROOT, rel)).filter(filter).sort()
  } catch {
    return []
  }
}

/** Absolute paths of every file under `relDir` whose name passes `matchFile`. */
function walk(relDir, matchFile) {
  const out = []
  const rec = (dir) => {
    let entries
    try {
      entries = readdirSync(dir, { withFileTypes: true })
    } catch {
      return
    }
    for (const e of entries.sort((a, b) => a.name.localeCompare(b.name))) {
      const p = join(dir, e.name)
      if (e.isDirectory()) rec(p)
      else if (matchFile(e.name)) out.push(p)
    }
  }
  rec(join(ROOT, relDir))
  return out
}

/** Route path from an App Router file, relative to `web/src/app` (POSIX slashes). */
function routeFrom(abs, stripLeaf) {
  const tail = abs.split(join(ROOT, 'web/src/app'))[1].replace(/\\/g, '/')
  const route = tail.replace(stripLeaf, '').replace('/(frontend)', '')
  return route === '' ? '/' : route
}

function collections() {
  return listDir('web/src/collections', (f) => f.endsWith('.ts')).map((f) => {
    const src = readFileSync(join(ROOT, 'web/src/collections', f), 'utf8')
    const slug = src.match(/slug:\s*['"]([^'"]+)['"]/)
    return `- \`${f}\`${slug ? ` — slug \`${slug[1]}\`` : ''}`
  })
}

function pipelines() {
  return listDir('ai/app/pipelines', (f) => f.endsWith('.py') && f !== '__init__.py').map(
    (f) => `- \`${f}\``,
  )
}

function endpoints() {
  let src = ''
  try {
    src = readFileSync(join(ROOT, 'ai/app/api/routes.py'), 'utf8')
  } catch {
    return []
  }
  const re = /@router\.(get|post|put|delete|patch)\(\s*['"]([^'"]+)['"]/g
  const out = []
  let m
  while ((m = re.exec(src))) out.push(`- \`${m[1].toUpperCase()} ${m[2]}\``)
  return out.sort()
}

function sitePages() {
  return walk('web/src/app/(frontend)', (f) => f === 'page.tsx')
    .map((abs) => `- \`${routeFrom(abs, /\/page\.tsx$/)}\``)
    .sort()
}

function webApiRoutes() {
  return walk('web/src/app/api', (f) => f === 'route.ts')
    .map((abs) => `- \`${routeFrom(abs, /\/route\.ts$/)}\``)
    .sort()
}

const section = (title, items) => `### ${title}\n${items.length ? items.join('\n') : '- (none)'}\n`

const body = [
  section('Payload collections — `web/src/collections/`', collections()),
  section('Site pages — `web/src/app/(frontend)/`', sitePages()),
  section('Web API routes — `web/src/app/api/`', webApiRoutes()),
  section('AI pipelines — `ai/app/pipelines/`', pipelines()),
  section('AI HTTP endpoints — `ai/app/api/routes.py`', endpoints()),
].join('\n')

const block =
  `${START}\n` +
  `<!-- Generated by scripts/gen-claude-map.mjs — do not edit by hand. Run: pnpm claude:map -->\n\n` +
  `${body}\n${END}`

const md = readFileSync(CLAUDE_MD, 'utf8')
const re = /<!-- AUTO-GEN:map START -->[\s\S]*?<!-- AUTO-GEN:map END -->/
if (!re.test(md)) {
  console.error('gen-claude-map: AUTO-GEN:map markers not found in CLAUDE.md')
  process.exit(1)
}
const next = md.replace(re, block)
if (next !== md) {
  writeFileSync(CLAUDE_MD, next)
  console.log('gen-claude-map: CLAUDE.md map updated')
} else {
  console.log('gen-claude-map: map already up to date')
}
